require("scripts/globals/monstertpmoves")
require("scripts/globals/status")
require("scripts/globals/msg")
require("scripts/globals/utils")

function onMobSpawn(pet)
    local master = pet:getMaster()        
    local mLvl = master:getMainLvl()
    local foodPower = 3 + (mLvl / 10)
    pet:addStatusEffect(tpz.effect.DEF_BOOST,foodPower*2.2,0,9600,0,0)
    pet:addStatusEffect(tpz.effect.STR_BOOST,foodPower*2,0,9600,0,0)
    pet:addStatusEffect(tpz.effect.INT_BOOST,foodPower,0,9600,0,0)
    pet:addStatusEffect(tpz.effect.MAX_HP_BOOST,foodPower*2,0,9600,0,0)
    pet:addStatusEffect(tpz.effect.ACCURACY_BOOST,foodPower,0,9600,0,0)
    pet:addStatusEffect(tpz.effect.DEFENSE_BOOST,foodPower,0,9600,0,0)
    local diff = (pet:getMaxHP() - pet:getHP())
    pet:addHP(diff)
end